# ╔═╡ 8f8414aa-094b-11eb-31dc-cfa0cd975387
filename = "hotels_topic.json"

# ╔═╡ 624afcd1-0593-4feb-891b-dc132c311269
samples_per_aspect = 3

# ╔═╡ bb8ec66e-094b-11eb-3fc3-73cea20f3440
js = JSON.parse(read(filename, String))

# ╔═╡ 329536a6-094c-11eb-0254-77af9f905a56
numdict(X) = Dict(x => i for (i,x) in enumerate(X|>unique|>sort))

# ╔═╡ 74f0d281-0e00-4414-82fe-ba1a2c48c1d3
asp2asp = Dict(
	"Activities" => "Activities", "Ambiance" => "Environment", "Amenities" => "Extra", "Bathroom" => "Bathroom", "Bed" => "Bed", "Breakfast" => "Breakfast", "Cleanliness" => "Clean", "Dinner" => "Dinner", "Drinks" => "Drinks", "Family-friendliness" => "Family", "Food" => "Food", "Hotel" => "Hotel", "Location" => "Location", "Maintenance" => "Maintenance", "Noise level" => "Noise", "Payment" => "Payment", "Public transport" => "Transport", "Reception" => "Reception", "Room" => "Room", "Safety" => "Safety", "Staff" => "Staff", "Value for money" => "Value", "View" => "View", "Wellness" => "Spa", "WiFi" => "Internet"
	)

# ╔═╡ c8753c12-094b-11eb-284c-a3d4a00e1dff
begin
	sentences, aspects, sentiments = js["sentences"], js["aspects"], js["sentiments"]
	aspects = [asp2asp[a] for a in aspects]
end

# ╔═╡ 9b38fffc-094d-11eb-2e7d-4971410e0dca
clean_sentences = [ replace(sent, "\n" => "")|>strip for sent in sentences]

# ╔═╡ 118721c2-094c-11eb-329e-675eca96ff3f
asp2num = numdict(aspects)

# ╔═╡ 59eed0f0-0c8e-11eb-1a7b-65ef55d86037
sort(asp2num)

# ╔═╡ 6d08b2e4-094d-11eb-2b90-d93886768231
sent2num = numdict(sentiments)

# ╔═╡ 7b01e631-d521-4f0f-9726-c297c26e9b9e
begin
	res = []
	for (i,(sentence, aspect)) in enumerate(zip(clean_sentences, aspects))
		push!(res, Dict("sentence" => sentence, "label" => aspect, "idx"=>i))
	end
	res
end

# ╔═╡ 569bb24f-4195-411b-a245-2af32b0cecc5
# Select a fixed number of samples of each aspect for the training set
begin
	train_res = []
	rest_res = []
	asp_count = Dict(asp => 0 for asp in values(asp2asp))
	for r in res
		if asp_count[r["label"]] < samples_per_aspect
			push!(train_res, r)
			asp_count[r["label"]] += 1
		else
			push!(rest_res, r)
		end
	end
	train_res
end

# ╔═╡ 8079039b-6474-4ec1-b783-3d713228b864
dev_res, test_res = splitobs(shuffleobs(rest_res, rng=MersenneTwister(42)), at=0.05)

# ╔═╡ 2e1dc168-4331-4720-b6df-fa25650bb770
begin
	open("./data/DO/train.jsonl", "w") do IO
		for r in train_res
			println(IO, json(r))
		end
	end
	
	open("./data/DO/dev.jsonl", "w") do IO
		for r in dev_res[1:200]
			println(IO, json(r))
		end
	end
	
	open("./data/DO/test.jsonl", "w") do IO
		for r in test_res
			println(IO, json(r))
		end
	end
  println("Conversion finished")
end

